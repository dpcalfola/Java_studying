public class Number {
    public static void main(String[] args) {
//        Calculating number
        System.out.println(6+2);
        System.out.println(6%4); // remainder(나머지)
        System.out.println(6*2);
        System.out.println(6/2);
        System.out.println(Math.pow(3,2)); // 3의 2제곱
        System.out.println(Math.random()); // 0 < result < 1
        System.out.println(Math.floor(Math.random()*7)); // 0 이상 7 미만의 정수 출력
        System.out.println((Math.abs(-6))); // 절대값 = absolute value
        

    }
}
