package objectOrientedProgramming;


class MyInstancePrint {

    public String delimiter ;

    public void A() {
        System.out.println(delimiter);
        System.out.println("A");
        System.out.println("A");
        System.out.println("");
    }

    public void B() {
        System.out.println(delimiter);
        System.out.println("B");
        System.out.println("B");
        System.out.println("");
    }



}


